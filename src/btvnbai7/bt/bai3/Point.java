package btvnbai7.bt.bai3;

public class Point {
    private float x = 0.0f;
    private float y = 0.0f;
public Point(){
}

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }
}
